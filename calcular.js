function Suma(){

    var n1 = Number(document.getElementById("n1").value);
    var n2 = Number(document.getElementById("n2").value);
    
    var T = n1 + n2;
    
    document.getElementById("T").value =T;
}
function Resta(){

    var n1 = Number(document.getElementById("n1").value);
    var n2 = Number(document.getElementById("n2").value);
    
    var T = n1 - n2;
    
    document.getElementById("T").value =T;
}
